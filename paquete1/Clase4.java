package paquete1;

public class Clase4 {
    private boolean x;
    private int y;

    public boolean getX() {
        return x;
    }

    public void setX(boolean x) {
        this.x = x;
    }
    
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Clase4 [x=" + x + " y= " + y + "]";
    }

}
